// Program 1: Calculate the Area of a Circle


const radius: number = 5; 
const pi: number = Math.PI; 


const area: number = pi * Math.pow(radius, 2);

console.log(`The area of the circle with a radius of ${radius} is ${area}.`);

// Program 2: Convert Celsius to Fahrenheit


const celsius: number = 25; 


const fahrenheit: number = (celsius * 9/5) + 32;

console.log(` ${celsius}°C is equal to ${fahrenheit}°F.`);
