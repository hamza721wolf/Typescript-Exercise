// Program 1: Calculate the Area of a Circle
var radius = 5;
var pi = Math.PI;
var area = pi * Math.pow(radius, 2);
console.log("The area of the circle with a radius of ".concat(radius, " is ").concat(area, "."));
// Program 2: Convert Celsius to Fahrenheit
var celsius = 25;
var fahrenheit = (celsius * 9 / 5) + 32;
console.log(" ".concat(celsius, "\u00B0C is equal to ").concat(fahrenheit, "\u00B0F."));
